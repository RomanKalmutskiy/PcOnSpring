package itea.ua;

public class ClassCpu implements Cpu {
	
	String name;
	Double frequency;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getFrequency() {
		return frequency;
	}
	public void setFrequency(Double frequency) {
		this.frequency = frequency;
	}

	

}
